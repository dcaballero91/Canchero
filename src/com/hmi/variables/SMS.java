package com.hmi.variables;


public class SMS {

    private int idsms;
    private String numCel;
    private String texto;
    private String fecha_int;
    
    public int getIdsms() {
        return idsms;
    }

    public void setIdsms(int idsms) {
        this.idsms = idsms;
    }

    public String getNumCel() {
        return numCel;
    }

    public void setNumCel(String numCel) {
        this.numCel = numCel;
    }

    public String getTexto() {
        return texto;
    }

    public void setTexto(String texto) {
        this.texto = texto;
    }

    
    

    public String getFecha_int() {
        return fecha_int;
    }

    public void setFecha_int(String fecha_int) {
        this.fecha_int = fecha_int;
    }
    

    
}